package com.example.jsonview;

public class EmpModal {

    // Properties to store employee data
    String name;
    String age;
    String city;

    // Getter for the employee's name
    public String getName() {
        return name;
    }

    // Setter for the employee's name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for the employee's age
    public String getAge() {
        return age;
    }

    // Setter for the employee's age
    public void setAge(String age) {
        this.age = age;
    }

    // Getter for the employee's city
    public String getCity() {
        return city;
    }

    // Setter for the employee's city
    public void setCity(String city) {
        this.city = city;
    }
}
